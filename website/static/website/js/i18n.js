// Get browser language
function getBrowserLanguage() {
    const browserLang = navigator.language || navigator.userLanguage;
    const shortLang = browserLang.split('-')[0];
    
    // Check if we support this language
    if (translations[shortLang]) {
        return shortLang;
    }
    
    // Default to French
    return 'fr';
}

// Get saved language or detect from browser
function getCurrentLanguage() {
    const savedLang = localStorage.getItem('obviotrans_lang');
    if (savedLang && translations[savedLang]) {
        return savedLang;
    }
    return getBrowserLanguage();
}

// Set language
function setLanguage(lang) {
    if (!translations[lang]) {
        console.error('Language not supported:', lang);
        return;
    }
    
    localStorage.setItem('obviotrans_lang', lang);
    applyTranslations(lang);
    updateLanguageSelector(lang);
    
    // Set RTL for Arabic
    if (lang === 'ar') {
        document.documentElement.setAttribute('dir', 'rtl');
        document.documentElement.setAttribute('lang', 'ar');
        document.body.classList.add('rtl');
    } else {
        document.documentElement.setAttribute('dir', 'ltr');
        document.documentElement.setAttribute('lang', lang);
        document.body.classList.remove('rtl');
    }
}

// Apply translations to DOM
function applyTranslations(lang) {
    const t = translations[lang];
    
    // Get all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            const tagName = el.tagName.toLowerCase();
            
            // Check if it's an input or textarea with a placeholder key
            if ((tagName === 'input' || tagName === 'textarea') && key.includes('placeholder')) {
                el.setAttribute('placeholder', t[key]);
            }
            // Check if it's a regular input/textarea (for labels use innerHTML)
            else if (tagName === 'input' || tagName === 'textarea') {
                // Don't change innerHTML for inputs, only placeholders
                if (el.hasAttribute('placeholder')) {
                    el.setAttribute('placeholder', t[key]);
                }
            }
            // For select options
            else if (tagName === 'option') {
                el.textContent = t[key];
            }
            // For all other elements
            else {
                el.innerHTML = t[key];
            }
        }
    });
    
    // Handle placeholders specifically (legacy support)
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            el.setAttribute('placeholder', t[key]);
        }
    });
    
    // Update WhatsApp link
    const whatsappLinks = document.querySelectorAll('a[href*="wa.me"]');
    whatsappLinks.forEach(link => {
        const baseUrl = link.href.split('?')[0];
        link.href = `${baseUrl}?text=${encodeURIComponent(t.whatsapp_message)}`;
    });
}

// Update language selector UI
function updateLanguageSelector(lang) {
    const selector = document.getElementById('lang-selector');
    if (selector) {
        selector.value = lang;
    }
    
    // Update current language display in header
    const currentLangDisplay = document.getElementById('current-lang');
    if (currentLangDisplay) {
        const langMap = { fr: 'FR', en: 'EN', ar: 'عربي' };
        currentLangDisplay.textContent = langMap[lang] || 'FR';
    }
    
    // Update active state on language buttons
    document.querySelectorAll('[data-lang]').forEach(btn => {
        btn.classList.remove('active', 'bg-accent', 'text-white');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active', 'bg-accent', 'text-white');
        }
    });
}

// Get translation
function t(key) {
    const lang = getCurrentLanguage();
    return translations[lang][key] || key;
}

// Initialize i18n on page load
document.addEventListener('DOMContentLoaded', function() {
    const lang = getCurrentLanguage();
    setLanguage(lang);
    
    // Add event listeners to language selector
    const selector = document.getElementById('lang-selector');
    if (selector) {
        selector.addEventListener('change', (e) => {
            setLanguage(e.target.value);
        });
    }
    
    // Add event listeners to language buttons
    document.querySelectorAll('[data-lang]').forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.preventDefault();
            setLanguage(btn.getAttribute('data-lang'));
        });
    });
});
